
class RadiusCombination
	{
		float trackRadius, targetRadius;
		
		public void setRadiusCombination(float cr, float tr)
		{
			trackRadius = cr;
			targetRadius = tr;
		}
		
		public float get_trackRadius()
		{
			return trackRadius;
		}
		public float get_targetRadius()
		{
			return targetRadius;
		}
		
	}